  /**
   * Ïù¥Î≤§Ìä∏ Î¶¨Ïä§ÎÑà Ïó∞Í≤∞
   */
  attachEventListeners() {
    if (!this.currentModal) return;
    
    // Îã´Í∏∞ Î≤ÑÌäº
    const closeBtn = this.currentModal.querySelector('.contact-close-btn');
    const cancelBtn = this.currentModal.querySelector('.contact-cancel-btn');
    const backdrop = this.currentModal.querySelector('.contact-modal-backdrop');
    
    closeBtn?.addEventListener('click', () => this.hide());
    cancelBtn?.addEventListener('click', () => this.hide());
    
    // Î∞±ÎìúÎ°≠ ÌÅ¥Î¶≠ÏúºÎ°ú Îã´Í∏∞
    backdrop?.addEventListener('click', (e) => {
      if (e.target === backdrop) {
        this.hide();
      }
    });
    
    // ESC ÌÇ§Î°ú Îã´Í∏∞
    const handleKeydown = (e) => {
      if (e.key === 'Escape') {
        this.hide();
        document.removeEventListener('keydown', handleKeydown);
      }
    };
    document.addEventListener('keydown', handleKeydown);
    
    // Ìèº Ï†úÏ∂ú
    const form = this.currentModal.querySelector('#contactSystemForm');
    form?.addEventListener('submit', (e) => this.handleSubmit(e));
  }
  
  /**
   * Ìèº Ï†úÏ∂ú Ï≤òÎ¶¨
   */
  async handleSubmit(e) {
    e.preventDefault();
    
    const form = e.target;
    const submitBtn = form.querySelector('.contact-submit-btn');
    const btnText = submitBtn.querySelector('.btn-text');
    const btnLoading = submitBtn.querySelector('.btn-loading');
    
    // Î°úÎî© ÏÉÅÌÉú
    submitBtn.disabled = true;
    btnText.style.display = 'none';
    btnLoading.style.display = 'inline';
    
    try {
      // Ìèº Îç∞Ïù¥ÌÑ∞ ÏàòÏßë
      const formData = this.collectFormData(form);
      
      // API Ï†ÑÏÜ°
      const result = await this.sendToAPI(formData);
      
      if (result.success) {
        this.showMessage('‚úÖ Î¨∏ÏùòÍ∞Ä ÏÑ±Í≥µÏ†ÅÏúºÎ°ú Ï†ÑÏÜ°ÎêòÏóàÏäµÎãàÎã§. Îπ†Î•∏ ÏãúÏùº ÎÇ¥Ïóê ÎãµÎ≥ÄÎìúÎ¶¨Í≤†ÏäµÎãàÎã§.', 'success');
        
        // ÏÑ±Í≥µ ÏΩúÎ∞±
        if (this.config.onSuccess) {
          this.config.onSuccess(formData, result);
        }
        
        // 3Ï¥à ÌõÑ ÏûêÎèô Îã´Í∏∞
        setTimeout(() => this.hide(), 3000);
        
      } else {
        this.showMessage('‚ùå ' + (result.message || 'Î¨∏Ïùò Ï†ÑÏÜ°Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§. Îã§Ïãú ÏãúÎèÑÌï¥Ï£ºÏÑ∏Ïöî.'), 'error');
        
        // Ïã§Ìå® ÏΩúÎ∞±
        if (this.config.onError) {
          this.config.onError(new Error(result.message || 'API Error'));
        }
      }
      
    } catch (error) {
      console.error('Contact form error:', error);
      this.showMessage('‚ùå ÎÑ§Ìä∏ÏõåÌÅ¨ Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§. Îã§Ïãú ÏãúÎèÑÌï¥Ï£ºÏÑ∏Ïöî.', 'error');
      
      if (this.config.onError) {
        this.config.onError(error);
      }
    }
    
    // Î°úÎî© ÏÉÅÌÉú Ìï¥Ï†ú
    submitBtn.disabled = false;
    btnText.style.display = 'inline';
    btnLoading.style.display = 'none';
  }
  
  /**
   * Ìèº Îç∞Ïù¥ÌÑ∞ ÏàòÏßë
   */
  collectFormData(form) {
    const formData = new FormData(form);
    const data = {};
    
    for (let [key, value] of formData.entries()) {
      data[key] = value.trim();
    }
    
    // Î©îÌÉÄÎç∞Ïù¥ÌÑ∞ Ï∂îÍ∞Ä
    data.source = this.config.source;
    data.url = window.location.href;
    data.userAgent = navigator.userAgent;
    data.timestamp = new Date().toISOString();
    
    return data;
  }
  
  /**
   * APIÎ°ú Îç∞Ïù¥ÌÑ∞ Ï†ÑÏÜ°
   */
  async sendToAPI(data) {
    const response = await fetch(this.config.apiUrl, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(data)
    });
    
    if (!response.ok) {
      throw new Error(`HTTP ${response.status}: ${response.statusText}`);
    }
    
    return await response.json();
  }
  
  /**
   * Î©îÏãúÏßÄ ÌëúÏãú
   */
  showMessage(message, type = 'info') {
    const messageEl = this.currentModal?.querySelector('#contactMessage');
    if (!messageEl) return;
    
    messageEl.textContent = message;
    messageEl.className = `contact-message ${type}`;
    messageEl.style.display = 'block';
  }
